export function t(locale: Locale | undefined, key: string, vars: Record<string, string | number> = {}) {
  const safeLocale: Locale = locale && dictionaries[locale] ? locale : "pt";
  const template = dictionaries[safeLocale][key] ?? dictionaries.pt[key] ?? key;
  return Object.entries(vars).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    template,
  );
}
